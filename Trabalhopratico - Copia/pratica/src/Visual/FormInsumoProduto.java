    public void atualizaTabela(){
        listObjetos.clear();
        listObjetos.addAll(daoInsumoProduto.getLista());
        int linha = listObjetos.size() - 1;
        if(linha >= 0){
            tblObjetos.setRowSelectionInterval(linha, linha);
            tblObjetos.scrollRectToVisible(tblObjetos.getCellRect(linha, linha, true));
        }
    }
    
    private void trataEdicao(boolean editando){
        btnCancelar.setEnabled(editando);
        btnSalvar.setEnabled(editando);
        btnEditar.setEnabled(!editando);
        btnExcluir.setEnabled(!editando);
        btnNovo.setEnabled(!editando);
        btnFechar.setEnabled(!editando);
        btnPrimeiro.setEnabled(!editando);
        btnUltimo.setEnabled(!editando);
        btnProximo.setEnabled(!editando);
        btnAnterior.setEnabled(!editando);
        cbIdProduto.setEditable(editando);
        cbIdInsumo.setEditable(editando);
        txtQuantidade.setEditable(editando);
        tblObjetos.setEnabled(!editando);
    }
    
    public boolean validaCampos(){
        if(!(cbIdProduto.getSelectedIndex() >= 0)){
            JOptionPane.showMessageDialog(null, "Digite ID do Produto!");
            cbIdProduto.requestFocus();
            return false;
        }
        
        if(!(cbIdInsumo.getSelectedIndex() >= 0)){
            JOptionPane.showMessageDialog(null, "Digite o ID do Insumo!");
        }
        
        if(!(txtQuantidade.getText().length() > 0)){
            JOptionPane.showMessageDialog(null, "Digite a quantidade!");
            txtQuantidade.requestFocus();
            return false;
        }
        
        return true;
    }
    
